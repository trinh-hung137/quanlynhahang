    private void loadData() {
        Admin ad = IOFileObject.readFile("src/controller/AD.DAT");
        userNameTextField.setText(ad.getUsername());
        passWordPasswordField.setText(ad.getPassword());
        cauHoi1TextField.setText(ad.getCauHoi1());
        traLoi1PasswordField.setText(ad.getTraLoi1());
        cauHoi2TextField.setText(ad.getCauHoi2());
        traLoi2PasswordField.setText(ad.getTraLoi2());
    }

    private boolean checkEmpty() {
        if (userNameTextField.getText().isBlank() || passWordPasswordField.getText().isBlank()
                || cauHoi1TextField.getText().isBlank()
                || traLoi1PasswordField.getText().isBlank()
                || cauHoi2TextField.getText().isBlank()
                || traLoi2PasswordField.getText().isBlank()) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng điền đủ thông tin", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return true;
        }
        return false;

    }
