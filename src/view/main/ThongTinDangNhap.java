    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        if (!checkEmpty()) {
            String file = "src/controller/AD.DAT";
            Admin admin = IOFileObject.readFile(file);
            admin.setUsername(userNameTextField.getText());
            admin.setPassword(passWordPasswordField.getText());
            admin.setCauHoi1(cauHoi1TextField.getText());
            admin.setCauHoi2(cauHoi2TextField.getText());
            admin.setTraLoi1(traLoi1PasswordField.getText());
            admin.setTraLoi2(traLoi2PasswordField.getText());
            IOFileObject.writeFile(file, admin);
            this.dispose();
        }
    }//GEN-LAST:event_saveButtonActionPerformed

