    private void suaBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_suaBTNActionPerformed
        // TODO add your handling code here:
        int row = tblNhanVien.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Hãy chọn nhân viên muốn sửa", "Thông báo", JOptionPane.PLAIN_MESSAGE);
        } else {
            try {
                int ma_nv = (int) tblNhanVien.getValueAt(row, 0);
                new SuaNhanVien(this, true, ma_nv).setVisible(true);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(QLNhanVien.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(QLNhanVien.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(
                        this, "<html><body><p style='width: 200px;'>" + ex.getMessage() + "</p></body></html>",
                        "Lỗi", JOptionPane.ERROR_MESSAGE);
                Logger.getLogger(QLNhanVien.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_suaBTNActionPerformed

    private void xoaBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xoaBTNActionPerformed
        // TODO add your handling code here:
        int row = tblNhanVien.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Hãy chọn nhân viên muốn xoá", "Thông báo", JOptionPane.PLAIN_MESSAGE);
        } else {
            if (JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xoá?", "Xác nhận",
                    JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE) == JOptionPane.YES_OPTION) {
                try {
                    int ma_nv = (int) tblNhanVien.getValueAt(row, 0);
                    NhanVienDAO.deleteNhanVien(ma_nv);
                    JOptionPane.showMessageDialog(this, "Xoá thành công", "Thành công", JOptionPane.INFORMATION_MESSAGE);
                    showNhanVien();
                } catch (ClassNotFoundException | IOException ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
                    Logger.getLogger(QLNhanVien.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(
                            this, "<html><body><p style='width: 200px;'>" + ex.getMessage() + "</p></body></html>",
                            "Lỗi", JOptionPane.ERROR_MESSAGE);
                    Logger.getLogger(QLNhanVien.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_xoaBTNActionPerformed

