    public void showNhanVien() throws ClassNotFoundException, IOException, FileNotFoundException, SQLException {
        nvList = NhanVienDAO.getAllNhanVien();
        tableModel.setRowCount(0);
        nvList.forEach(nv -> {
            tableModel.addRow(new Object[]{
                nv.getMa_nv(), nv.getHoTen(),
                new SimpleDateFormat("dd/MM/yyyy").format(nv.getNgay_sinh()),
                nv.getSdtString(), nv.getEmail(), nv.getLuong()
            });
        });
    }

