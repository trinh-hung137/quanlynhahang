    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int row = tblMonAn.getSelectedRow();
        if (row >= 0) {
            if (JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc muốn xoá", "Xác nhận",
                    JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE) == JOptionPane.YES_OPTION) {
                try {
                    int ma_ma = (int) tblMonAn.getValueAt(row, 0);
                    MonAnDAO.deleteMonAn(ma_ma);
                    JOptionPane.showMessageDialog(rootPane, "Xoá thành công", "Thành công", JOptionPane.INFORMATION_MESSAGE);
                    showMonAn();
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(QLMonAn.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(QLMonAn.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
//                    JOptionPane.showMessageDialog(
//                            this, "<html><body><p style='width: 200px;'>" + ex.getMessage() + "</p></body></html>",
//                            "Lỗi", JOptionPane.ERROR_MESSAGE);
                    JOptionPane.showMessageDialog(this, "Món ăn đã tồn tại trong ít nhất 1 hoá đơn.",
                            "Không thể xoá", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Hãy chọn món ăn muốn xoá", "Thông báo", JOptionPane.PLAIN_MESSAGE);
        }
    }//GEN-LAST:event_btnXoaActionPerformed

