    private boolean checkEmpty() {
        if (txtTenMon.getText().isBlank() || txtDonGia.getText().isBlank() || txtDonVi.getText().isBlank()) {
            return true;
        }
        return false;
    }

    private boolean checkValid() {
        if (!txtDonGia.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Đơn giá nhập số", "Lỗi định dạng", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        return true;
    }

