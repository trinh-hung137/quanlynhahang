    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (checkEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng điền đủ thông tin", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else {
            if (checkValid()) {
                try {
                    // TODO add your handling code here:
                    String ten_mon = txtTenMon.getText().trim();
                    int don_gia = Integer.parseInt(txtDonGia.getText().trim());
                    String don_vi = txtDonVi.getText().trim();
                    MonAn ma = new MonAn(0, ten_mon, don_gia, don_vi);
                    MonAnDAO.addMonAn(ma);
                    JOptionPane.showMessageDialog(rootPane, "Thêm thành công", "Thành công", JOptionPane.INFORMATION_MESSAGE);
                    father.showMonAn();
                    this.dispose();
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(ThemMonAn.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(ThemMonAn.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(
                            this, "<html><body><p style='width: 200px;'>" + ex.getMessage() + "</p></body></html>",
                            "Lỗi", JOptionPane.ERROR_MESSAGE);
                    Logger.getLogger(ThemMonAn.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

