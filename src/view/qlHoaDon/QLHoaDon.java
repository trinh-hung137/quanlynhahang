    public void showHoaDon() throws ClassNotFoundException, IOException, FileNotFoundException, SQLException {
        List<HoaDon> hdList = HoaDonDAO.getAllHoaDon();
        tableModel.setRowCount(0);
        hdList.forEach(hd -> {
            tableModel.addRow(new Object[]{
                hd.getMa_hd(), hd.getMa_kh(),
                new SimpleDateFormat("dd/MM/yyyy").format(hd.getNgay()),
                hd.getChiet_khau(),
                hd.getThanh_tien()
            });
        });
    }

